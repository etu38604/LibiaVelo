package ModelPackage;

public class SearchModel {

    private StationModel stationModel;
    private EmployeeModel employeeModel;
    private BikeModel bikeModel;
    private HistoricalModel historicalModel;
    private LocalityModel localityModel;
    private TransportOrderModel transportOrderModel;

    public StationModel getStationModel() {
        return stationModel;
    }

    public void setStationModel(StationModel stationModel) {
        this.stationModel = stationModel;
    }

    public EmployeeModel getEmployeeModel() {
        return employeeModel;
    }

    public void setEmployeeModel(EmployeeModel employeeModel) {
        this.employeeModel = employeeModel;
    }

    public BikeModel getBikeModel() {
        return bikeModel;
    }

    public void setBikeModel(BikeModel bikeModel) {
        this.bikeModel = bikeModel;
    }

    public HistoricalModel getHistoricalModel() {
        return historicalModel;
    }

    public void setHistoricalModel(HistoricalModel historicalModel) {
        this.historicalModel = historicalModel;
    }

    public LocalityModel getLocalityModel() {
        return localityModel;
    }

    public void setLocalityModel(LocalityModel localityModel) {
        this.localityModel = localityModel;
    }

    public TransportOrderModel getTransportOrderModel() {
        return transportOrderModel;
    }

    public void setTransportOrderModel(TransportOrderModel transportOrderModel) {
        this.transportOrderModel = transportOrderModel;
    }
}
